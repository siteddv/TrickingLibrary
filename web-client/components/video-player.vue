<template>
  <div class="video-container">
    <div class="play-button" :class="{'hide': playing}" @click.stop="playing = !playing">
      <v-icon size="78">mdi-play</v-icon>
    </div>
    <video ref="video"
           muted loop
           :src="video"
           :poster="thumb"
           preload="none"
           playsinline
    ></video>
  </div>
</template>

<script>

export default {
  name: "video-player",
  props: {
    video: {
      required: true,
      type: String,
    },
    thumb: {
      required: true,
      type: String,
    }
  },
  data: () => ({
    playing: false
  }),
  watch: {
    playing: function (v) {
      if (v) {
        this.$refs.video.play()
      } else {
        this.$refs.video.pause()
      }
    }
  }
}
</script>

